alert('Leitura de três valores e apresentação da soma dos quadrados')
var numero1=parseFloat(prompt('Insira o primeiro valor'))
quadrado1=numero1*numero1
var numero2=parseFloat(prompt('Insira o segundo valor'))
quadrado2=numero2*numero2
var numero3=parseFloat(prompt('Insira o terceiro valor'))
quadrado3=numero3*numero3

soma=quadrado1+quadrado2+quadrado3

alert('A soma dos quadrados é igual a '+soma)